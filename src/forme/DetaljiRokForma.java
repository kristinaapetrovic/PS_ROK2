    private void popuniComboBox() {
        
        List<Predmet> lista=Controller.getInstance().vratiPredmete();
        for(Predmet p: lista){
            jComboBoxPredmeti.addItem(p);
        }
    }

    private void popuniTabelu() {
        List<Prijava> lista=Controller.getInstance().vratiPrijavePoRoku(rok);
        ModelTabelePrijava mtp=new ModelTabelePrijava(lista);
        jTablePrijave.setModel(mtp);
    
    }
    
