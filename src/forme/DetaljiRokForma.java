    private void jButtonFiltrirajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonFiltrirajActionPerformed
        
        //filter prema predmetu
        
        Predmet predmet=(Predmet) jComboBoxPredmeti.getSelectedItem();
        
        List<Prijava> lista=Controller.getInstance().vratiPrijavePoRokuIPredmetu(rok, predmet);
        
        
        ModelTabelePrijava mtp=new ModelTabelePrijava(lista);
        jTablePrijave.setModel(mtp);
        
    }//GEN-LAST:event_jButtonFiltrirajActionPerformed

    private void jButtonPonistiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPonistiActionPerformed
      
        int sr=jTablePrijave.getSelectedRow();
        if(sr==-1){
            JOptionPane.showMessageDialog(this,"Izaberite prijavu!");
            return;
        }
        ModelTabelePrijava mt=(ModelTabelePrijava) jTablePrijave.getModel();
        Prijava prijava=mt.getLista().get(sr);
        int odg=JOptionPane.showConfirmDialog(this, "Da li zelite da ponistite ovu prijavu?", "Provera", JOptionPane.YES_NO_OPTION);
        if(odg==JOptionPane.YES_OPTION){
            
            boolean uspesno=Controller.getInstance().obrisiPrijavu(prijava);
            if(uspesno){
                JOptionPane.showMessageDialog(this, "Prijava je ponistena");
                popuniTabelu();
            }else{
                JOptionPane.showMessageDialog(this, "Greska pri brisanju");
            }
            
            
        }
        
        
        
        
    }//GEN-LAST:event_jButtonPonistiActionPerformed

