    private void popuniFormu() {
        SimpleDateFormat format=new SimpleDateFormat("dd.MM.yyyy.");
        jTextFieldNaziv.setText(rok.getNaziv());
        jTextFieldPocetak.setText(format.format(rok.getDatumPocetka()));
        jTextFieldZavrsetak.setText(format.format(rok.getDatumZavrsetka()));
        
        
    
    }

